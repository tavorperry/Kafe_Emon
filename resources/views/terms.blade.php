                <ol ><li><p>חלק מהשירותים המוצעים לציבור במערכת טעונים רישום במערכת. במסגרת הרישום תתבקש לעדכן פרטים אודותייך כגון: שם פרטי, שם משפחה, כתובת דואר אלקטרוני פעילה, וכן לבחור סיסמא אישית. חלק מן הפרטים האמורים יסומנו כ"שדות חובה" שבלא מסירת נתונים אלו לא ניתן יהיה לבצע את הרישום ויכול כי שירותים מסוימים יוכלו להינתן אך ורק לאחר רישום. בחלק מהשירותים, יכול ותתבקש להוסיף ולמסור נתונים/פרטים נוספים, המתחייבים מאופי השירות או כי לשיקול דעת האגודה, ניתן יהיה להעניק שירותים אלו לאחר מסירת הנתונים/פרטים הללו.</p>

                    </li>
                    <li><p>במקרה של שינוי פרטים יש לעדכן את מערכת קפה אמון באמצעות פנייה בדוא"ל (מוזכר לעיל) או באמצעות טופס "צור קשר".</p>

                    </li>
                    <li><p>במהלך הרישום למערכת הלקוח מאשר הסכמה לאישור קבלת הודעות פוש ("push notifications"), התראות אימיילים וניוזלטרים ממערך קפה אמון וכן מהאגודה שעשויים להיחשב כדברי פרסומת, כקבוע בתיקון מספר 40 לחוק התקשורת (בזק ושידורים) התשמ"ב 1982, סעיף 30 א`. <br />
                            באפשרותך להפסיק קבלת הודעות אלו כאמור בכל עת באמצעות לינק להסרה מרשימת התפוצה בתחתית המיילים הנשלחים וכן להפסיק באמצעות פניה לאגודה דרך האימייל, האתר או דף הפייסבוק של האגודה. (כל בקשה להסרה תטופל תוך 7 ימי עסקים).<br />
                            קניין רוחני:</p>

                    </li>
