                    </li>
                </ol><p><b>ד. אחריות מערכת קפה אמון, יוצרי המערכת האגודה</b></p>

                <ol ><li><p>אין אגודת הסטודנטים, מערכת קפה אמון, יוצרי המערכת, בעלי המערכת ו/או מפעילי המערכת ו/או מי מטעמה לא ישאו בכל אחריות לכל נזק שייגרם כתוצאה משימוש במערכת ומכל הצגה או פרסום של תכנים המופיעים במערכת, לרבות: תקלות במערכת ההפעלה ו/או בכל תכנה המצויה במכשיר ו/או כל בעיית חומרה ו/או וירוסים ו/או תוכנות עוינות ו/או לאיכות או מהירות השימוש באפליקציה.</p>

                    </li>
                    <li><p>השימוש והרכישה באתר מוגנת על ידי טכנולוגיות מתקדמות שמטרתן למנוע גניבת מידע אודותיכם מגורמים זרים. על אף האמור לעיל הינכם מצהירים כי לא תהיה לכם כל טענה ו/או דרישה ו/או תביעה כנגד מערכת קפה אמון או כנגד בעליה ומפעילי האתר עקב פגיעה או תקלה או שיבושים במקרה של חשיפת מידע בלתי מורשות וחדירה בלתי חוקית למאגרי מידע באתר זה.<br />
                            מערכת קפה אמון מצהירה כי אין בכוונתה לעשות כל שימוש לרעה בפרטי המידע אודותיכם ו/או להעבירם לגוף שלישי כלשהו ללא אישורכם (למעט מקרים משפטיים).</p>

                    </li>
                    <li><p>רכישה באתר – רכישת מוצרים ושירותים באתר מותרת לכל אדם שהינו תושב ישראל והינו מעל גיל 18 ובבעלותו כרטיס אשראי בתוקף , תעודת זהות וכתובת מגורים בישראל. במידה והרוכש מתחת לגיל 18 או אינו זכאי לבצע פעולות משפטיות ללא אישור אפוטרופוס יראו את שימושו באתר כאילו קיבל אישור אפוטרופוס. </p>

                    </li>
                    <li><p>שימוש בפרטים פיקטיביים ו/או גנובים ו/או ביצוע רכישות על ידי קטינים ללא אישור אפוטרופוס מהווה עבירה פלילית.</p>

                    </li>
                    <li><p>התכנים במערכת ניתנים לשימוש כמו שהם (AS IS). לא תהיה למשתמש במערכת כל טענה, תביעה או דרישה כלפי מערכת קפה אמון, האגודה ו/או מי מטעמה בגין תכונות של התכנים, יכולותיהם, מגבלותיהם, התאמתם לצרכים אינדיבידואלים של משתמשים שונים. לפיכך, השימוש במערכת ייעשה על אחריותו הבלעדית והמלאה של המשתמש.</p>

                    </li>
                </ol><p><b>ה. שינויים במערכת והפסקת השירות</b></p>

                <ol ><li><p>מערכת קפה אמון רשאית לשנות מעת לעת את מבנה המערכת, המראה שלה ועיצובה, ותהא רשאית לשנות כל היבט אחר הכרוך במערכת– והכל, בלא צורך בהודעה מוקדמת לציבור המשתמשים בה מראש.</p>

